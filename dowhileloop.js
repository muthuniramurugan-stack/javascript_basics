// A do...while loop is a control structure that executes a block of code first, and then checks a condition to decide whether to repeat.
// This means the loop always runs at least one time, no matter what.
//Syntax
/*
do {
    // code
} while (condition);
*/
let i = 10;

do {
    console.log(i); // 10
    i++;
} while (i < 5);  